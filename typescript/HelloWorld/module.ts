//  Modules, "." can be sued as separator for sub modules
module Geometry {
    export class Square {
        constructor(public sideLength: number = 0) { }
        area() {
            Math.pow(this.sideLength, 2);
        }
    }
}

let s1 = new Geometry.Square(5);

// Local alias for referencing a module
import G = Geometry;

let s2 = new G.Square(10);